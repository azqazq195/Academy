
public class Exam1 {
	public static void main(String[] args) {
		// 변수의 선언 : 컴퓨터에게 데이터를 저장할 공간을 달라는 명령
		int num1;
		// 변수에 데이터 저장
		num1 = 100;
		// 변수 선언과 동시에 저장
		long num2 = 8234567890123456789L;
		double num3 = 128.4d;
		float num4 = 3.14f;
		byte t = 100;
		short s = 250;
		char c = '가';
		boolean b = true;
		
		// 변수에 저장된 데이터 확인
		// 문자열 + 데이터 => "문자열데이터"로 문자열과 데이터가 결합된 문자열이 만들어짐
		System.out.println("num1 = " + num1);  // "num1 = 100"
		System.out.println("num2 = " + num2); 
		System.out.println("num3 = " + num3); 
		System.out.println("num4 = " + num4); 
		System.out.println("t = " + t); 
		System.out.println("s = " + s); 
		System.out.println("c = " + c); 
		System.out.println("b = " + b); 
	}
}









