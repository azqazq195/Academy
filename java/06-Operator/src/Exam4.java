
public class Exam4 {
	public static void main(String[] args) {
		// 변수값 1증가 시키기
		int plus_num = 1;
		System.out.println(plus_num);
		
		plus_num = plus_num + 1;
		System.out.println(plus_num);
		plus_num += 1;
		System.out.println(plus_num);
		plus_num++;
		System.out.println(plus_num);
		++plus_num;
		System.out.println(plus_num);
		System.out.println("------------------");
		
		// 변수값 1감소 시키기
		int minus_num = 5;
		System.out.println(minus_num);
		minus_num = minus_num - 1;
		System.out.println(minus_num);
		minus_num -= 1;
		System.out.println(minus_num);
		minus_num--;
		System.out.println(minus_num);
		--minus_num;
		System.out.println(minus_num);
	}
}







