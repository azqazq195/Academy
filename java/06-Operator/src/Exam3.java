
public class Exam3 {
	public static void main(String[] args) {
		// 대입 연산자 : =  +=  -=  *=  /=   %=
		// 변수명 = 데이터;
		int source = 100;
		System.out.println(source);
		
		source += 100;		//source = source + 100;
		System.out.println(source);
		
		source -= 100;		//source = source - 100;
		System.out.println(source);
		
		source *= 2;		//source = source * 2;
		System.out.println(source);
		
		source /= 2;		//source = source / 2;
		System.out.println(source);
		
		source %= 2;		//source = source % 2;
		System.out.println(source);
	}
}








