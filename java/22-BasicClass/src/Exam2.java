  
public class Exam2 {
	public static void main(String[] args) {
		int max = Math.max(100,9999);	// 최대값 구하기
		System.out.println(max);	
		int min = Math.min(100,9999);	// 최소값 구하기
		System.out.println(min);
		int abs = Math.abs(-123);		// 절대값
		System.out.println(abs);
		long num = Math.round(3.123456);// 소수점 반올림 -> 리턴값은 1
		System.out.println(num);
	}
}
